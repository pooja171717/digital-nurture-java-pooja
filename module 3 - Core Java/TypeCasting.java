class TypeCasting
{
    public static void main(String[] args)
    {
        double num1 = 25.75;

        int num2 = (int) num1;

        System.out.println("Double Value: " + num1);
        System.out.println("Converted Int Value: " + num2);

        int a = 50;

        double b = (double) a;

        System.out.println("Integer Value: " + a);
        System.out.println("Converted Double Value: " + b);
    }
}